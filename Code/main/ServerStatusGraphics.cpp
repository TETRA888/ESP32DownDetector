/*
    This function sets up the display and prints the credits
    @param None 
    @return None
*/
void setupDisplay() {
  pinMode(13, OUTPUT);
  digitalWrite(13, HIGH);
  Wire.setPins(14,12);
  display.begin(SSD1306_SWITCHCAPVCC, 0x3C);
  display.clearDisplay();
  printCredits();
  delay(2000);
  display.clearDisplay();
  display.display();
  printLoveSequence();
  display.clearDisplay();
  drawScrollingHeart();
  delay(2000);
}
