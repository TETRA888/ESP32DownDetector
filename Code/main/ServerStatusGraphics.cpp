/*
    This function prints a scrolling heart animation to show love
    @param None 
    @return None
*/
void drawScrollingHeart(){
  for(int i = 0; i < 56; i++){
    display.clearDisplay();
    display.drawXBitmap(i, 0, heart_bits, heart_width, heart_height, 1);
    display.display();
  }

  for(int i = 56; i > 0; i--){
    display.clearDisplay();
    display.drawXBitmap(i, 0, heart_bits, heart_width, heart_height, 1);
    display.display();
  }
}

